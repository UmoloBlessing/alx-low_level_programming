#include <stdio.h>

/**
 * main - Entry point of the program
 *
 * Return: 0 on success
 */
int main(void)
{
	int zero = '0';
	int newline = '\n';

	/* Print numbers 0 to 9 using putchar */
	putchar(zero);
	putchar(newline);
	putchar(zero + 1);
	putchar(newline);
	putchar(zero + 2);
	putchar(newline);
	putchar(zero + 3);
	putchar(newline);
	putchar(zero + 4);
	putchar(newline);
	putchar(zero + 5);
	putchar(newline);
	putchar(zero + 6);
	putchar(newline);
	putchar(zero + 7);
	putchar(newline);
	putchar(zero + 8);
	putchar(newline);
	putchar(zero + 9);
	putchar(newline);

	return (0);
}
